const add = (firstNum, secondNum) => {
    return (firstNum + secondNum);
}

const sub = (firstNum, secondNum) => {
    return (firstNum - secondNum);
}

const multiply = (firstNum, secondNum) => {
    return (firstNum * secondNum);
}

const divide = (firstNum, secondNum) => {
    return (firstNum / secondNum);
}

module.exports = {
    add,
    sub,
    multiply,
    divide
}